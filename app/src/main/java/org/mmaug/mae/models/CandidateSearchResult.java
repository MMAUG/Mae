public class CandidateSearchResult {
  private String id;
  private String name;
  private String party;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getParty() {
    return party;
  }

  public void setParty(String party) {
    this.party = party;
  }
}
